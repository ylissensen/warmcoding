package warmcoding.classes;

public class user {
	private int userIdentificationCode;
	private String userPassWord;
	private String userName;
	public int getUserIdentificationCode() {
		return userIdentificationCode;
	}
	public void setUserIdentificationCode(int userIdentificationCode) {
		this.userIdentificationCode = userIdentificationCode;
	}
	public String getUserPassWord() {
		return userPassWord;
	}
	public void setUserPassWord(String userPassWord) {
		this.userPassWord = userPassWord;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	

}
